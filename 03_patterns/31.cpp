#include <iostream>
using namespace std;

int main(){

    int n;
    cout<<"enter a number : ";
    cin>>n;

    for (int i=1; i<=n; i++){

        int a=n;

        for (int j=1; j<=(n-1); j++){
            cout<<a;

            if (j<=(i-1)){
                a--;
            }
        }


        //*************************************************************************************************************************************************
        
        
        a=(n-i+1);

        for (int j=1; j<=n; j++){
            cout<<a;

            if (j>=(n-i+1)){
                a++;
            }
        }


        cout<<endl;
    }


//**************************************************************************************************************************************************


    for (int i=2; i<=n; i++){

        int a=n;

        for (int j=1; j<=(n-1); j++){
            cout<<a;

            if (j<=(n-i)){
                a--;
            }
        }


        //**************************************************************************************************************************************************


        a=i;

        for (int j=1; j<=n; j++){
            cout<<a;

            if (j>=i){
                a++;
            }
        }

        cout<<endl;
    }


    return 0;
}